local items_data_table = 
{
    ["category_items_all"] = {},
    ["category_items_pets"] = {},
    ["category_items_particles"] = {},
 --   ["category_items_gems"] = {},
    ["category_items_subscribes"] = {},
    ["category_items_chests"] = {},
    ["category_items_skins_players"] = {},
    ["category_items_skins_towers"] = {},
    ["category_items_skin_wisps"] = {},
    ["category_items_sounds"] = {},
	["category_items_label"] = {},
   -- ["category_items_sprays"] = {},
    ["category_items_recommended"] = {},
}

for _, item_info in pairs(Shop.DonateList) do
    if string.find(item_info[5], "pet_") then
        table.insert(items_data_table["category_items_pets"], item_info)
    end
    if string.find(item_info[5], "particle_") then
        table.insert(items_data_table["category_items_particles"], item_info)
    end
   -- if item_info[2] == "gem" then
  --      table.insert(items_data_table["category_items_gems"], item_info)
  --  end
    if string.find(item_info[5], "subscribe_") then
        table.insert(items_data_table["category_items_subscribes"], item_info)
    end
    if string.find(item_info[5], "chest_") then
        table.insert(items_data_table["category_items_chests"], item_info)
    end
    if string.find(item_info[5], "skin_") and not string.find(item_info[5], "skin_wisp_") then
        table.insert(items_data_table["category_items_skins_players"], item_info)
    end
    if string.find(item_info[5], "tower_") or string.find(item_info[5], "true_sight_tower") or string.find(item_info[5], "high_true_sight_tower") or string.find(item_info[5], "flag") then
        table.insert(items_data_table["category_items_skins_towers"], item_info)
    end
    if string.find(item_info[5], "skin_wisp_") then
        table.insert(items_data_table["category_items_skin_wisps"], item_info)
    end
    if string.find(item_info[5], "sounds_") then
        table.insert(items_data_table["category_items_sounds"], item_info)
    end
	if string.find(item_info[5], "label_") then
        table.insert(items_data_table["category_items_label"], item_info)
    end
 --   if string.find(item_info[5], "spray_") then
--        table.insert(items_data_table["category_items_sprays"], item_info)
  --  end
    if Shop.ITEM_RECOMMEND_IDS[item_info[1]] then
        table.insert(items_data_table["category_items_recommended"], item_info)
    end
    table.insert(items_data_table["category_items_all"], item_info)
end

CustomNetTables:SetTableValue("Shop", "data_categories", items_data_table)
CustomNetTables:SetTableValue("Shop", "data_chest", Shop.Chests)
CustomNetTables:SetTableValue("Shop", "donate_rewards",  Shop.donate_rewards )
CustomNetTables:SetTableValue("Shop", "battlepass_xp", Shop.exp_battlepass )
CustomNetTables:SetTableValue("Shop", "free_rewards",  Shop.free_rewards  )